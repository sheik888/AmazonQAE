package polymorphismprograms;

 class Calculator {
	//add two integers
	
	int add(int a, int b) {
		return a+b;
		
	}

// add three integer
	int add(int a, int b, int c) {
		return a+b+c;
		
	}
	
//add two double
	double add(double a, double b) {
		return a+b;
	
	}
	//concatenate two strings
	String add(String a, String b) {
		return a+b;
	


	
 }
 }

package FlowControlLab;

public class ForLoopDemo {
public static void main(String[] args) {
	
	//for loop
	
for (int  i = 1; i <= 5; i++) {
		
		
		
		
		System.out.println("Count: " + i);
		
	}


//nested for loop

for (int i = 1; i<= 3; i++) {
	
	for (int j= 1; j<= 3; j++) {
		System.out.println(i * j + " ");
}
	System.out.println();

}
	
}
}
	
	
